    print("Less    P1 P2            ? {}".format(IsLess(GetAbsis("1"),GetOrdinat("1"),GetAbsis("2"),GetOrdinat("2"))))
    print("Greate  P1 P2            ? {}".format(IsGreater(GetAbsis("1"),GetOrdinat("1"),GetAbsis("2"),GetOrdinat("2"))))
