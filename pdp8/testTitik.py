    print("P4 Kuadran Ke            : {} <-Sudah diset diatas dengan 4,4".format(Kuadran(GetAbsis("4"),GetOrdinat("4"))))
    print("Next P1                  : ({}, {})".format(NextX(GetAbsis("1"),"1"),NextY(GetOrdinat("1"),"1")))
    print("Next P2                  : ({}, {})".format(NextX(GetAbsis("2"),"2"),NextY(GetOrdinat("2"),"2")))
    print("PlusDelta P3 (2, 2)      : ({})".format(PlusDelta(GetAbsis("3"),GetOrdinat("3"),2,2,"3")))
    print("Mirror P4 SbX            : ({})".format(MirrorOf(GetAbsis("4"),GetOrdinat("4"),"SbX","4")))
    print("Geser P1 (2, 3)          : ({})".format(Geser(GetAbsis("1"),GetOrdinat("1"),2,3,"1")))
    print("Mirror P2 SbX            : ({})".format(MirrorOf(GetAbsis("2"),GetOrdinat("2"),"SbX","2")))
